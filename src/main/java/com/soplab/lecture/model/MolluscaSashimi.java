package com.soplab.lecture.model;

public class MolluscaSashimi extends Sashimi {
    String molluscaName;
    Double molluscaPrice;
    String molluscaType;

    public String getMolluscaName() {
        return molluscaName;
    }

    public void setMolluscaName(String molluscaName) {
        this.molluscaName = molluscaName;
    }

    public Double getMolluscaPrice() {
        return molluscaPrice;
    }

    public void setMolluscaPrice(Double molluscaPrice) {
        this.molluscaPrice = molluscaPrice;
    }

    public String getMolluscaType() {
        return molluscaType;
    }

    public void setMolluscaType(String molluscaType) {
        this.molluscaType = molluscaType;
    }
}
